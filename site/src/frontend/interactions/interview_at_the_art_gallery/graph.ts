const remainingLocationOptions = (state: ArtGalleryState) => {
  const remaining_options = [];
  if (!state.thought1) {
    remaining_options.push({
      text: "In his wallet?",
      next: "where-a",
      stateEffect(state: ArtGalleryState) {
        return {
          ...state,
          thought1: true,
        };
      },
    });
  }
  if (!state.thought2) {
    remaining_options.push({
      text: "In his car?",
      next: "where-b",
      stateEffect(state: ArtGalleryState) {
        return {
          ...state,
          thought2: true,
        };
      },
    });
  }
  if (!state.thought3) {
    remaining_options.push({
      text: "In his office?",
      next: "where-c",
      stateEffect(state: ArtGalleryState) {
        return {
          ...state,
          thought3: true,
        };
      },
    });
  }
  remaining_options.push({
    text: "In his study?",
    next: "where-d",
  });
  return remaining_options;
};

