const GROUP_PUZZLES_BY_UUID: Record<string, Puzzle> = GROUPED_PUZZLES.reduce<
  Record<string, Puzzle>
>((acc, group) => {
  acc[group.firstPuzzle.uuid] = group.firstPuzzle;
  acc[group.lastPuzzle.uuid] = group.lastPuzzle;
  for (const subgroup of group.subgroups) {
    for (const puzzle of subgroup.puzzles) {
      acc[puzzle.uuid] = puzzle;
    }
  }
  return acc;
}, {});

