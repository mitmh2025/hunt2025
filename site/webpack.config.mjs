import path from "path";
import url from "url";
import {
  WebpackRscClientPlugin,
  WebpackRscServerPlugin,
  createWebpackRscClientLoader,
  createWebpackRscServerLoader,
  createWebpackRscSsrLoader,
  webpackRscLayerName,
} from "@mfng/webpack-rsc";
import MiniCssExtractPlugin from "mini-css-extract-plugin";
import { WebpackManifestPlugin } from "webpack-manifest-plugin";

const currentDirname = path.dirname(url.fileURLToPath(import.meta.url));
const outputDirname = path.join(currentDirname, "dist");
const outputManifestDirname = outputDirname;

const reactServerManifestFilename = path.join(
  outputManifestDirname,
  `react-server-manifest.json`,
);

const reactClientManifestFilename = path.join(
  outputManifestDirname,
  `react-client-manifest.json`,
);

const reactSsrManifestFilename = path.join(
  outputManifestDirname,
  `react-ssr-manifest.json`,
);

class LogValue {
  constructor(name, m) {
    this.name = name;
    this.m = m;
  }
  apply(compiler) {
    compiler.hooks.emit.tap("LogValue", () => {
      console.log(this.name, this.m);
    });
  }
}

/**
 * @param {unknown} _env
 * @param {{readonly mode?: import('webpack').Configuration['mode']}} argv
 * @return {import('webpack').Configuration[]}
 */
export default function createConfigs(_env, argv) {
  const { mode } = argv;
  const dev = mode === "development";

  const clientReferencesMap = new Map();
  const serverReferencesMap = new Map();

  const rscServerLoader = createWebpackRscServerLoader({
    clientReferencesMap,
    serverReferencesMap,
  });

  const rscSsrLoader = createWebpackRscSsrLoader({ serverReferencesMap });
  const rscClientLoader = createWebpackRscClientLoader({ serverReferencesMap });

  const cssRule = {
    test: /\.css$/,
    use: [
      MiniCssExtractPlugin.loader,
      {
        loader: "css-loader",
        options: {
          modules: {
            localIdentName: dev
              ? "[local]__[hash:base64:5]"
              : "[hash:base64:7]",
            auto: true,
          },
        },
      },
      // TODO: {loader: "postcss-loader",},
    ],
  };

  const pngRule = (publicPath) => ({
    test: /\.png$/,
    type: "asset/resource",
    generator: {
      outputPath: "assets/",
      publicPath: `${publicPath}/assets/`,
      filename: "[hash][ext][query]",
    },
  });

  const resolve = {
    extensions: [".ts", ".tsx", "..."],
    extensionAlias: {
      ".js": [".ts", ".js"],
      ".mjs": [".mts", ".mjs"],
    },
    alias: {
      "@": path.join(currentDirname, "src"),
    },
  };

  const serverSwcLoader = {
    // .swcrc can be used to configure swc
    loader: "swc-loader",
  };

  const serverConfig = {
    name: "server",
    target: "node",
    // Server code imports JSON manifests that are generated by the isolated client build,
    // so we need to run after the isolated client build.
    dependencies: ["isolated"],
    entry: {
      server: "./src/main.ts",
    },
    output: {
      path: outputDirname,
      // If we hook assets up to a CDN:
      // publicPath: 'https://cdn.example.com/assets/[fullhash]/',
      filename: "[name]-bundle.js",
      publicPath: "/assets/",
      libraryTarget: "module",
      chunkFormat: "module",
      devtoolModuleFilenameTemplate: (
        /** @type {{ absoluteResourcePath: string; }} */ info,
      ) => info.absoluteResourcePath,
    },
    module: {
      rules: [
        {
          resource: [/\/server\/rsc\//, /\/server\/routes\/.*\.tsx/],
          layer: webpackRscLayerName,
        },
        {
          resource: /\/server\/shared\//,
          layer: `shared`,
        },
        {
          // Work around bug in websocket-express
          test: /websocket-express/,
          resolve: { conditionNames: ["require"] },
        },
        {
          issuerLayer: webpackRscLayerName,
          resolve: { conditionNames: [`react-server`, `...`] },
        },
        {
          oneOf: [
            {
              issuerLayer: webpackRscLayerName,
              test: /\.m?[jt]sx?$/,
              use: [rscServerLoader, serverSwcLoader],
            },
            {
              test: /\.m?[jt]sx?$/,
              use: [rscSsrLoader, serverSwcLoader],
            },
          ],
        },
        cssRule,
        // TODO: support importing other kinds of assets, and aliases for
        // the results of the browser build bundles
        pngRule(""),
      ],
      // Add modules as appropriate
    },
    resolve,
    externalsPresets: { node: true },
    // Webpack needs to see any libraries that include React components, so we can't use a blanket nodeExternals here.
    // This is a list of our heavyweight server-only dependencies.
    externals: ["express", "websocket-express", "better-sqlite3", "pg-native", "knex", "swagger-ui-express", "redis"],
    plugins: [
      // server-main.css is not used, but required by MiniCssExtractPlugin.
      new MiniCssExtractPlugin({
        filename: `[contenthash].css`,
        runtime: false,
      }),
      new WebpackRscServerPlugin({
        clientReferencesMap,
        serverReferencesMap,
        serverManifestFilename: path.relative(
          outputManifestDirname,
          reactServerManifestFilename,
        ),
      }),
    ],
    experiments: {
      outputModule: true,
      layers: true,
    },
    devtool: dev ? "source-map" : `source-map`,
    mode,
    stats: {
      errorDetails: true,
      // TODO: stats
    },
  };

  const clientOutputDirname = path.join(outputDirname, `static/client`);

  const clientOutput = {
    filename: dev ? "[name].[contenthash:16].js" : "[contenthash:16].js",
    path: clientOutputDirname,
    // Can't clean when multiple outputs share a directory.
    clean: false,
    publicPath: "/client/",

    devtoolModuleFilenameTemplate: (
      /** @type {{ absoluteResourcePath: string; }} */ info,
    ) => info.absoluteResourcePath,
  };

  // TODO: Disable client-side source maps in production
  const clientDevtool = "source-map";

  const clientConfig = {
    name: "client",
    dependencies: ["server"],
    entry: {
      main: "./src/frontend/client/main.tsx",
    },
    target: "web",
    output: clientOutput,
    devtool: clientDevtool,
    module: {
      rules: [
        {
          test: /\.m?[jt]sx?$/,
          use: [rscClientLoader, "swc-loader"],
        },
        cssRule,
        pngRule("/client"),
      ],
    },
    resolve,
    plugins: [
      new MiniCssExtractPlugin({
        filename: dev ? "[name].[contenthash:16].css" : "[contenthash:16].css",
        runtime: false,
      }),
      new WebpackManifestPlugin({
        fileName: path.join(outputManifestDirname, "client-css-manifest.json"),
        publicPath: "/client/",
        filter: (file) => file.path.endsWith(".css"),
      }),
      new WebpackManifestPlugin({
        fileName: path.join(outputManifestDirname, "client-js-manifest.json"),
        publicPath: "/client/",
        filter: (file) => file.path.endsWith(".js"),
      }),
      new WebpackRscClientPlugin({
        clientReferencesMap,
        clientManifestFilename: path.relative(
          clientOutputDirname,
          reactClientManifestFilename,
        ),
        ssrManifestFilename: path.relative(
          clientOutputDirname,
          reactSsrManifestFilename,
        ),
      }),
      new LogValue(`clientReferencesMap`, clientReferencesMap),
      new LogValue(`serverReferencesMap`, serverReferencesMap),
    ],
    experiments: {
      layers: true,
    },
  };

  const isolatedConfig = {
    name: "isolated",
    entry: {
      shadow_diamond: "./src/frontend/rounds/shadow_diamond/client.tsx",
    },
    target: "web",
    output: clientOutput,
    devtool: clientDevtool,
    module: {
      rules: [
        {
          test: /\.m?tsx?$/,
          exclude: /(node_modules)/,
          use: ["swc-loader"],
        },
        cssRule,
        pngRule("/client"),
      ],
    },
    resolve,
    plugins: [
      new MiniCssExtractPlugin({
        filename: dev ? "[name].[contenthash:16].css" : "[contenthash:16].css",
        runtime: false,
      }),
      new WebpackManifestPlugin({
        fileName: path.join(
          outputManifestDirname,
          "isolated-css-manifest.json",
        ),
        publicPath: "/client/",
        filter: (file) => file.path.endsWith(".css"),
      }),
      new WebpackManifestPlugin({
        fileName: path.join(outputManifestDirname, "isolated-js-manifest.json"),
        publicPath: "/client/",
        filter: (file) => file.path.endsWith(".js"),
      }),
      new WebpackManifestPlugin({
        fileName: path.join(
          outputManifestDirname,
          "isolated-asset-manifest.json",
        ),
        publicPath: "/client",
        filter: (file) =>
          !file.path.endsWith(`.js`) && !file.path.endsWith(".css"),
      }),
    ],
    experiments: {
      outputModule: true,
      layers: true,
    },
    // ...
  };
  return [serverConfig, clientConfig, isolatedConfig];
}
